"""PyVax Transformer — Python AST → Flattened Solidity Source Code.

Converts a ContractState (from PythonASTAnalyzer) into verified-ready,
single-file Solidity source that can be compiled with solc and verified
on Snowtrace/Etherscan.

Usage:
    from avax_cli.transpiler import PythonASTAnalyzer
    from avax_cli.transformer import generate_solidity, python_to_verified_solidity

    # Option A: From ContractState
    state = PythonASTAnalyzer().analyze_contract(python_source)
    sol_source = generate_solidity(state, contract_name="MyToken")

    # Option B: Full pipeline
    result = python_to_verified_solidity(python_source)
    print(result["solidity"])  # Flattened .sol
    print(result["abi"])       # JSON ABI
"""

