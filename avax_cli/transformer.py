# ── Solidity type mapping ─────────────────────────────────────────────

PYTHON_TO_SOL_TYPE = {
    "uint256": "uint256",
    "int": "uint256",
    "address": "address",
    "str": "address",
    "bool": "bool",
    "mapping": "mapping(address => uint256)",
    "string": "string",
}

# Default Solidity version for output
SOL_VERSION = "0.8.24"
SOL_LICENSE = "MIT"


